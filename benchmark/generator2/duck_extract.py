import duckdb
import re
from datetime import date, datetime

def duckdb_type_to_mysql(dtype: str) -> str:
    """簡單轉換 DuckDB 資料型態到 MySQL"""
    dtype = dtype.lower()
    if dtype.startswith('varchar') or dtype == 'string':
        return 'TEXT'
    if dtype.startswith('int') or dtype == 'integer':
        return 'INT'
    if dtype == 'bigint':
        return 'BIGINT'
    if dtype == 'double' or dtype == 'float':
        return 'DOUBLE'
    if dtype == 'boolean':
        return 'BOOLEAN'
    if dtype == 'date':
        return 'DATE'
    if dtype == 'timestamp':
        return 'DATETIME'
    # 其他類型可以依需求擴充
    return 'TEXT'

def escape_sql_value(val):
    """簡單逃脫字串，處理None和字串內單引號"""
    if val is None:
        return 'NULL'
    if isinstance(val, (date, datetime)):
        # 日期時間型態直接轉字串再包引號
        return f"'{val.isoformat()}'"
    if isinstance(val, str):
        val = val.replace("'", "''")
        return f"'{val}'"
    return str(val)

def generate_create_table(con, table_name):
    """根據 DuckDB 表 schema 產生 MySQL CREATE TABLE 語句"""
    info = con.execute(f"DESCRIBE {table_name}").fetchall()
    columns = []
    for col_name, col_type, _, _, _, _ in info:
        mysql_type = duckdb_type_to_mysql(col_type)
        columns.append(f"`{col_name}` {mysql_type}")
    columns_sql = ",\n  ".join(columns)
    return f"CREATE TABLE `{table_name}` (\n  {columns_sql}\n);"

def generate_insert_statements(con, table_name, batch_size=1000):
    """產生 INSERT INTO 語句，分批輸出"""
    rows = con.execute(f"SELECT * FROM {table_name}").fetchall()
    if not rows:
        return []
    columns = [desc[0] for desc in con.description]
    inserts = []
    for i in range(0, len(rows), batch_size):
        batch = rows[i:i+batch_size]
        values = []
        for row in batch:
            escaped = [escape_sql_value(v) for v in row]
            values.append(f"({', '.join(escaped)})")
        sql = f"INSERT INTO `{table_name}` ({', '.join(columns)}) VALUES\n" + ",\n".join(values) + ";"
        inserts.append(sql)
    return inserts

def export_tpcds_to_sql(filename, sf=0.01):
    con = duckdb.connect()
    con.execute("INSTALL tpcds;")
    con.execute("LOAD tpcds;")
    con.execute(f"CALL dsdgen(sf={sf});")  # 產生資料

    tables = con.execute("""
        SELECT table_name FROM information_schema.tables
        WHERE table_schema = 'main'
        ORDER BY table_name
    """).fetchall()

    with open(filename, 'w', encoding='utf-8') as f:
        f.write(f"-- TPC-DS Data dump generated by DuckDB (scale factor {sf})\n\n")
        for (table_name,) in tables:
            print(f"Exporting table: {table_name}")
            # CREATE TABLE
            create_sql = generate_create_table(con, table_name)
            f.write(create_sql + "\n\n")
            # INSERT INTO
            inserts = generate_insert_statements(con, table_name)
            for ins in inserts:
                f.write(ins + "\n\n")

if __name__ == "__main__":
    export_tpcds_to_sql("tpcds_data_dump.sql", sf=0.01)
    print("Export completed! File: tpcds_data_dump.sql")

